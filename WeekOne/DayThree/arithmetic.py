# arithmetic.py

def addition(number_one, number_two):
    print(number_one + number_two)

def subtraction(number_one, number_two): # -
    print(number_one - number_two)

def multiplication(number_one, number_two): # *
    print(number_one * number_two)

def division(number_one, number_two): # /
    print(number_one / number_two)